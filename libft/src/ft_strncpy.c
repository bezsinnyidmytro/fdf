#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, unsigned long int len)
{
	unsigned long int	i;

	i = 0;
	while (src[i] != '\0' && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (i != len)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
